    public void GenerarApaRevistas() {
        String autor, anio, tituloarticulo, titulorevista, apa, volumen, numero, paguinacion;
        autor = "";
        if (contLista == 1) {
            autor = autor.concat(apellidos[0].concat(", ").concat(String.valueOf(nombres[0].charAt(0)).concat(". ")));
        } else {
            for (int i = 0; i < contLista; i++) {

                if (i == contLista - 2) {
                    autor = autor.concat(apellidos[i].concat(", ").concat(String.valueOf(nombres[i].charAt(0)).concat(". ")));
                } else if (i == contLista - 1) {
                    autor = autor.concat("& ".concat(apellidos[i]).concat(", ").concat(String.valueOf(nombres[i].charAt(0)).concat(". ")));
                } else {
                    autor = autor.concat(apellidos[i].concat(", ").concat(String.valueOf(nombres[i].charAt(0)).concat("., ")));
                }
            }
        }
        contLista = 0;
        anio = String.valueOf(tfAnio.getText());
        tituloarticulo = tfTitulo.getText();
        titulorevista = tfTituloRevista.getText();
        volumen = tfVolumen.getText();
        numero = tfNumero.getText();
        paguinacion = tfPaguinacion.getText();
        if (tfAnio.getText().length() == 0 || tfTitulo.getText().length() == 0 || tfTituloRevista.getText().length() == 0
                || tfVolumen.getText().length() == 0 || tfNumero.getText().length() == 0 || tfPaguinacion.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "FALTA LLENAR ALGUN CAMPO");
        } else {
            apa = autor.concat("(").concat(anio).concat("). ".concat(tituloarticulo)).concat(". ").concat(titulorevista).concat(". ")
                    .concat(volumen).concat("(").concat(numero).concat(")").concat(", ").concat("p.").concat(paguinacion).concat(".");
            tfResultado.setText(apa.concat("\n").concat(tfResultado.getText()));
        }

    }
