    public String[] AgregarAutor(String nombre[], String apellido[]) {
        String[] autor = new String[100];
        jComboBoxRevista.addItem(tfApellido.getText().toUpperCase().trim().concat(" ").concat(tfNombre.getText().toUpperCase().trim()));
        apellidos[contLista] = tfApellido.getText();
        nombres[contLista] = tfNombre.getText();
        tfApellido.setText(null);
        tfNombre.setText(null);
        contLista++;
        return autor;
    }

