    public void GenerarApaLibro() {
        String autor, titulo, anio, lugar, editorial, apa;
        autor = "";
        if (contLista == 1) {
            autor = autor.concat(apellidos[0].concat(", ").concat(String.valueOf(nombres[0].charAt(0)).concat(". ")));
        } else {
            for (int i = 0; i < contLista; i++) {

                if (i == contLista - 2) {
                    autor = autor.concat(apellidos[i].concat(", ").concat(String.valueOf(nombres[i].charAt(0)).concat(". ")));
                } else if (i == contLista - 1) {
                    autor = autor.concat("& ".concat(apellidos[i]).concat(", ").concat(String.valueOf(nombres[i].charAt(0)).concat(". ")));
                } else {
                    autor = autor.concat(apellidos[i].concat(", ").concat(String.valueOf(nombres[i].charAt(0)).concat("., ")));
                }
            }
        }
        contLista = 0;
        anio = String.valueOf(tfAnio.getText());
        titulo = tfTitulo.getText();
        lugar = tfLugar.getText();
        editorial = tfEditorial.getText();
        if (tfAnio.getText().length() == 0 || tfTitulo.getText().length() == 0 || tfLugar.getText().length() == 0 || tfEditorial.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "FALTA LLENAR ALGUN CAMPO");
        } else {
            apa = autor.concat("(").concat(anio).concat("). ").concat(titulo).concat(". ").concat(lugar).concat(": ").concat(editorial).concat(".");
            jtResultado.setText(apa.concat("\n").concat(jtResultado.getText()));
        }
    }

