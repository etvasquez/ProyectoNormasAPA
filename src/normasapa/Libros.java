    private void bntAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAgregarActionPerformed
        String apellido[] = new String[100];
        String nombre[] = new String[100];
        String autor[] = new String[100];
        if (tfNombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "FALTA LLENAR CAMPO NOMBRE");
        } else if (tfApellido.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "FALTA LLENAR CAMPO APELLIDO");
        } else {
            autor = AgregarAutor(nombre, apellido);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_bntAgregarActionPerformed
    public String[] AgregarAutor(String nombre[], String apellido[]) {
        String[] autor = new String[100];
        jComboBoxLista.addItem(tfApellido.getText().toUpperCase().trim().concat(" ").concat(tfNombre.getText().toUpperCase().trim()));
        apellidos[contLista] = tfApellido.getText();
        nombres[contLista] = tfNombre.getText();
        tfApellido.setText(null);
        tfNombre.setText(null);
        contLista++;
        return autor;
    }
